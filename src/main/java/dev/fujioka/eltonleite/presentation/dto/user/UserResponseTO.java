package dev.fujioka.eltonleite.presentation.dto.user;

import java.time.LocalDate;

public class UserResponseTO {

    private Long id;

    private String username;

    private LocalDate dateBirth;

    public UserResponseTO(Long id, String username, LocalDate dateBirth) {
        super();
        this.id = id;
        this.username = username;
        this.dateBirth = dateBirth;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public LocalDate getDateBirth() {
        return dateBirth;
    }

    public void setDateBirth(LocalDate dateBirth) {
        this.dateBirth = dateBirth;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        UserResponseTO other = (UserResponseTO) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return String.format("UserResponseTO [id=%s, username=%s, dateBirth=%s]", id, username, dateBirth);
    }

}
